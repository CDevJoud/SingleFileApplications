static double SwapBytes(double val) {
	Qword temp, value = val;
	memcpy(&temp, &val, sizeof(double));       // Reinterpret as uint64_t
	temp = ((value >> 56) & 0x00000000000000FFULL) |
		((value >> 40) & 0x000000000000FF00ULL) |
		((value >> 24) & 0x0000000000FF0000ULL) |
		((value >> 8) & 0x00000000FF000000ULL) |
		((value << 8) & 0x000000FF00000000ULL) |
		((value << 24) & 0x0000FF0000000000ULL) |
		((value << 40) & 0x00FF000000000000ULL) |
		((value << 56) & 0xFF00000000000000ULL);                      // Swap the bytes
	memcpy(&val, &temp, sizeof(double));       // Reinterpret back to double
	return val;
}


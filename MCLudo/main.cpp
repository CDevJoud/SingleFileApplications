static MCUUID UUIDStringToBytes(const std::string& uuidStr) {

	// Example input: "82be945a-36c1-4b38-975e-9366c24c6c42"
	std::string hex;
	hex.reserve(32);

	for (char c : uuidStr) {
		if (std::isxdigit(c)) {
			hex.push_back(c);
		}
	}

	if (hex.length() != 32) {
		throw std::invalid_argument("Invalid UUID string format");
	}
	MCUUID uuid{};
	for (size_t i = 0; i < 16; ++i) {
		std::string byteStr = hex.substr(i * 2, 2);
		uuid[i] = static_cast<Byte>(std::stoi(byteStr, nullptr, 16));
	}
	return uuid;
}


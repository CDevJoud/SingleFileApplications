	void SendChunks(Int32 x, Int32 z) {
		Packet chunk = 0x21;

		// === CHUNK COORDINATES ===
		bool groundUpContinuous = true;
		int section = 15;

		uint16_t blockID = 0x01;
		uint8_t meta = 0;

		// === BLOCK DATA ===
		std::vector<uint8_t> chunkData;

		// 4096 blocks, each 2 bytes
		for (int i = 0; i < 4096; i++) {
			uint8_t byte1 = ((ChunkBlocks[i] << 4) | ChunkBlocksMeta[i]);
			uint8_t byte2 = ChunkBlocks[i] >> 4;
			chunkData.push_back(byte1);
			chunkData.push_back(byte2);
		}

		// --- Block Light (2048 bytes) ---
		// 0xFF = maximum light for two 4-bit values per byte
		chunkData.insert(chunkData.end(), 2048, 0x00);

		// --- Sky Light (2048 bytes) ---
		// Only needed in Overworld (true for default dimension)
		chunkData.insert(chunkData.end(), Skylight, Skylight + 2048);

		// --- Biome (256 bytes) ---
		chunkData.insert(chunkData.end(), 256, 0x01);

		// === CHUNK PACKET ===
		VarInt32 chunkSize = chunkData.size();

		uint16_t primaryBitMask = ((1 << section) - 1);  // Only one section (y=0)
		chunk << x
			<< z
			<< Byte(groundUpContinuous)
			<< Word(primaryBitMask)
			<< chunkSize;

		// Append raw chunk section data
		chunk.GetRawBytes().insert(
			chunk.GetRawBytes().end(),
			chunkData.begin(),
			chunkData.end()
		);

		// === SEND TO CLIENT ===
		m_client->Send(chunk);
	}
